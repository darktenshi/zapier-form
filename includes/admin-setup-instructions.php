<h2>Setup Instructions</h2>

<h3>Getting Started</h3>
<p>To begin, you'll need a Zapier account and a Zapier Webhook URL. Follow these steps to get set up.</p>

<h3>Step-by-Step Guide</h3>
<p>To set up your Zapier webhook for use with the MaidCentral Zapier integration, you can follow these summarized steps:</p>

<ol>
    <li><strong>Sign Up or Log In to Zapier:</strong><br>
    Visit Zapier and either sign up for a new account or log in to your existing one.</li>
    <li><strong>Create a New Zap:</strong><br>
    Once logged in, click on "Create Zap" to start a new automation.<br>
    You'll be prompted to choose a trigger event. For this plugin, you typically choose "Webhooks by Zapier" as the trigger.</li>
    <li><strong>Set Up the Webhook:</strong><br>
    Choose "Catch Hook" under Webhooks by Zapier.<br>
    Zapier will provide you with a unique webhook URL. This is the URL you will copy and paste into the Zapier Webhook URL field in the plugin's admin settings.</li>
    <li><strong>Add Your Zapier Key:</strong><br>
    Go to the General Settings tab in the plugin's admin page.<br>
    Locate the "Zapier Key" field and enter your Zapier API key. This key is crucial for secure communication between your WordPress site and Zapier.</li>
    <li><strong>Test Your Trigger:</strong><br>
    Once the webhook is set up in your plugin and you've submitted a test form, return to Zapier and test the trigger to ensure it catches the data from the form submission correctly.</li>
    <li><strong>Choose an Action App:</strong><br>
    After successfully setting up and testing the webhook, choose MaidCentral as the action app.<br>
    You can configure the Zap to perform actions like sending an email, creating a lead in MaidCentral, or sending a text message.</li>
    <li><strong>Finalize and Turn On Your Zap:</strong><br>
    Once everything is set up and tested, give your Zap a name, turn it on, and it will start automating tasks based on form submissions from your WordPress site.</li>
</ol>

