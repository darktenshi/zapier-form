<h2>Plugin Information</h2>

<h3>Plugin Summary</h3>
<p>Zapier Form Modal allows you to automate lead capture and follow-up tasks through Zapier. It's tailored for MaidCentral users who want to streamline their operations, but may work for other programs like L27.</p>

<h3>Use Cases for Zapier Integration</h3>
<ul>
    <li><strong>Automated Email Notifications:</strong> Set up a Zap to send yourself or your team an email whenever a form is submitted. This ensures no lead or inquiry goes unnoticed.</li>
    <li><strong>Customer Engagement:</strong> Send an automated email or SMS to the customer after form submission, providing confirmation or additional information about your services.</li>
    <li><strong>Lead Management:</strong> Automatically create a lead in MaidCentral to manage your sales pipeline more efficiently. You can also trigger follow-up actions based on this lead data.</li>
    <li><strong>Internal Communication:</strong> Use Zapier to send notifications or tasks to your employees when a form is submitted, ensuring everyone is on the same page.</li>
</ul>

