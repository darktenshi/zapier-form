<h3>Use Cases for Your Zap</h3>
<ul>
    <li><strong>Send Yourself an Email:</strong> Automatically receive an email notification each time a form is submitted. This can help you keep track of new leads or inquiries.</li>
    <li><strong>Send the Customer an Email:</strong> Configure your Zap to send a thank-you email or confirmation message to the customer after they submit the form. Personalizing this email can enhance customer experience.</li>
    <li><strong>Send a Text Message:</strong> Use Zapier to send an SMS notification to the customer or your employees when a form is submitted. This is useful for immediate follow-ups or task notifications.</li>
    <li><strong>Create a Lead in MaidCentral:</strong> Automatically create a new lead in MaidCentral (maidcentral.com) when the form is submitted. This streamlines your sales or service process by eliminating manual data entry.</li>
    <li><strong>Send Text to Employees:</strong> Configure a Zap to notify your team via SMS when a new lead is captured, ensuring quick follow-up and assignment.</li>
</ul>

<h3>Troubleshooting</h3>
<p>If you're not receiving data in Zapier, ensure that your webhook URL is correct and that the form is configured correctly.</p>
